package edu.vn.models;

import java.io.Serializable;

/**
 *
 * @author starbuck
 */
//ho tro luu file
 public class Students implements Serializable {
    private String id;
    private String name;
    private String age;
    private String email;

    public Students() {
    }

    public Students(String id, String name, String age, String email) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    } 
}
